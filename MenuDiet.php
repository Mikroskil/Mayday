<h2>Menu Makanan Diet Menurunkan Berat Badan</h2>
<p><em><strong>Menu pagi hari</strong></em>
salah satu cara Diet yang tepat adalah saat sarapan pagi Anda bisa mengganti kuning telur ataupun keju yang biasa digunakan dalam campuran omelet dengan sayur-sayuran seperti brokoli, sawi ataupun jamur. Sayur-sayuran memiliki kalori yang rendah namun cukup mengenyangkan hingga siang hari.
</p>
<p><em><strong> Menu siang hari </strong></em>
Setelah setengah hari beraktifitas, di rumah ataupun dalam pekerjaan, tentu tubuh memerlukan energi untuk memulihkan tenaga. Juga perut sudah terasa lapar dan diminta segera siisi. Kita tidak harus sengsara dengan menahan lapar. Kalau biasanya nasi putih adalah makanan wajib, saat menjalani program diet, kita bisa menggantinya dengan beras merah.</p>
<p><em><strong>Menu malam hari</strong></em>
Pada saat makan malam, kita bisa mengkonsumsi kenis ikan yang banyak mengandung asam omega 3. Juga akan emmbantu kelancaran peredaran darah. Sebaiknya, disertai dengan sayuran hijau kaya akan serat yang akan sangat membantu program diet, sekaligus sangat baik untuk pencernaan dan kesehatan tubuh.
</p>
